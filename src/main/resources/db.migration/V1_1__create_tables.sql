CREATE TABLE IF NOT EXISTS CARDS (
    ID INTEGER NOT NULL ,
    CARD_NAME VARCHAR(255),
    CARD_TYPE VARCHAR(255),
    CARD_VALUE INTEGER,
    IF_NATIONAL INTEGER,
    CARD_COST INTEGER,
    NATIONALITY VARCHAR(255),
    ATTACK INTEGER,
    DEFENCE INTEGER,
    HEALTH_POINTS INTEGER,
    VEHICLE_TYPE VARCHAR(255),
    SKILL1 VARCHAR(255),
    SKILL1_VALUE INTEGER,
    SKILL2 VARCHAR(255),
    SKILL2_VALUE INTEGER,
    MEDAL_VALUE INTEGER,
    IF_DEFENDABLE INTEGER,
    DEFENDING_CARD_ID INTEGER,
    PRIMARY KEY (ID)
);
